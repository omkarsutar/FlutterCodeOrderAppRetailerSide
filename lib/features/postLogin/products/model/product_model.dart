      if (productId != null) ModelProductFields.productId: productId,
      ModelProductFields.productType: productType,
      ModelProductFields.productName: productName,
      if (productNameHindi != null) ModelProductFields.productNameHindi: productNameHindi,
      ModelProductFields.productWeightValue: productWeightValue,
      ModelProductFields.productWeightUnit: productWeightUnit,
      ModelProductFields.purchaseRateForRetailer: purchaseRateForRetailer,
      ModelProductFields.mrp: mrp,
      ModelProductFields.packagingType: packagingType,
      if (piecesPerOuter != null)
