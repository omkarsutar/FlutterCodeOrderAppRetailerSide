      productWeightUnit:
          map[ModelProductFields.productWeightUnit]?.toString() ?? '',
      purchaseRateForRetailer:
          double.tryParse(
            map[ModelProductFields.purchaseRateForRetailer]?.toString() ?? '',
          ) ??
