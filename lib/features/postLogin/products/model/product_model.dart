    required this.productWeightValue,
    required this.productWeightUnit,
    required this.purchaseRateForRetailer,
    required this.mrp,
    required this.packagingType,
    this.piecesPerOuter,
    required this.isOuter,
    required this.isActive,
    required this.isAvailable,
    required this.qtyInDecimal,
    this.productImage,
    this.createdBy,
    this.updatedBy,
    this.createdAt,
    this.updatedAt,
  });

  factory ModelProduct.fromMap(Map<String, dynamic> map) {
    return ModelProduct(
      productId: map[ModelProductFields.productId]?.toString(),
      productType: map[ModelProductFields.productType]?.toString() ?? '',
      productName: map[ModelProductFields.productName]?.toString() ?? '',
