import '../../../../core/services/entity_service.dart';

class ModelShopFields {
  static const String table = 'shops';
  static const String tableViewWithForeignKeyLabels = 'view_shops';

  static const String shopId = 'shop_id';
  static const String shopName = 'shop_name';
  static const String shopsPrimaryRoute = 'shops_primary_route';
  static const String shopNote = 'shop_note';
  static const String hiddenNote = 'hidden_note';
  static const String shopMobile1 = 'shop_mobile_1';
  static const String shopMobile2 = 'shop_mobile_2';
  static const String shopPersonName = 'shop_person_name';
  static const String isActive = 'is_active';
  static const String shopLocationUrl = 'shop_location_url';
  static const String shopLandmark = 'shop_landmark';
  static const String shopAddress = 'shop_address';
  static const String shopPhotoId = 'shop_photo_id';
  static const String shopPhotoUrl = 'shop_photo_url';
  static const String shopLat = 'shop_lat';
  static const String shopLong = 'shop_long';
  static const String createdAt = 'created_at';
  static const String updatedAt = 'updated_at';
  static const String visitOrder = 'visit_order';

  static const Map<String, String> labels = {
    shopId: 'Shop',
    shopName: 'Shop Name',
    shopsPrimaryRoute: 'Primary Route',
    shopNote: 'Shop Note',
    hiddenNote: 'Hidden Note',
    shopMobile1: 'Mobile 1',
